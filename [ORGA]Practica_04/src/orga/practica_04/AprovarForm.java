    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!jList1.isSelectionEmpty()){
            String a = jList1.getSelectedValue();
            for(Jugador j: LogIn.admin.getListaJugadores()){
                if(j.getUsername().equals(a)){
                    j.setEstado(true);
                    desplegarLista();                    
                    JOptionPane.showMessageDialog(null,"El usuario "+j.getUsername()+" ha sido aceptado!");
                    return;
                }
            }
            //System.out.println(a);
        }else{
            JOptionPane.showConfirmDialog(null,"Selecciona un usuario para aprovar su solicitud!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
          if(!jList1.isSelectionEmpty()){
            String b = jList1.getSelectedValue();
            int contador = 0;
            for(Jugador j: LogIn.admin.getListaJugadores()){
                if(j.getUsername().equals(b)){
                    JOptionPane confirm = new JOptionPane("¿Está de seguro que desea denegar el acceso a este usuario?",
                    JOptionPane.QUESTION_MESSAGE, JOptionPane.YES_NO_OPTION);
                    JDialog answer = confirm.createDialog(null, "Denegar Jugadores");
                    answer.setVisible(true);
                    if(Integer.parseInt(confirm.getValue().toString())==JOptionPane.YES_OPTION){
                        LogIn.admin.getListaJugadores().remove(contador);
                        desplegarLista();
                    }
                    
                    return;
                }
                contador ++;
            }
        }else{
            JOptionPane.showConfirmDialog(null,"Selecciona un usuario para aprovar su solicitud!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

